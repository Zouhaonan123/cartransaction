package com.zhn.cartransaction.perobj;

public class CheckInfo {
    private String carId;
    private String carOrigin;
    private Integer accidentNum;
    private String inspection;
    private String userId;

    public String getCarId() {
        return carId;
    }

    public void setCarId(String carId) {
        this.carId = carId;
    }

    public String getCarOrigin() {
        return carOrigin;
    }

    public void setCarOrigin(String carOrigin) {
        this.carOrigin = carOrigin;
    }

    public Integer getAccidentNum() {
        return accidentNum;
    }

    public void setAccidentNum(Integer accidentNum) {
        this.accidentNum = accidentNum;
    }

    public String getInspection() {
        return inspection;
    }

    public void setInspection(String inspection) {
        this.inspection = inspection;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }


}
